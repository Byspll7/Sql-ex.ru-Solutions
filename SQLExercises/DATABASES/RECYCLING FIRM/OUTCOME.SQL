IF EXISTS (SELECT * FROM DBO.SYSOBJECTS WHERE ID = OBJECT_ID(N'DBO.OUTCOME') AND OBJECTPROPERTY(ID, N'ISTABLE') = 1)
	BEGIN
		DROP TABLE OUTCOME
	END
GO

IF (EXISTS (SELECT * FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_SCHEMA = 'DBO' AND  TABLE_NAME = 'OUTCOME'))
	BEGIN
		DROP TABLE OUTCOME
	END
GO

IF OBJECT_ID (N'OUTCOME', N'U') IS NOT NULL 
	BEGIN
		DROP TABLE OUTCOME
